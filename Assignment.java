// write a program for Assignment operator
public class Assignment
{
  public static void main(String args[])
 {
     int num1=60;
     System.out.println(num1);
     num1+=5;
     System.out.println(num1);
     num1-=5;
     System.out.println(num1);
     num1*=5;
     System.out.println(num1);
     num1/=5;
     System.out.println(num1);
     num1%=5;
     System.out.println(num1);
 }
}
